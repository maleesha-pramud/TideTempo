    private void init() {
        if (isEditing) {
            headerLabel.setText("Edit Client Details");
            addClientBtn.setText("Update Client");
            loadClientData();
        } else {
            headerLabel.setText("Add New Client");
            addClientBtn.setText("Add Client");
        }
    }

    private void loadClientData() {
        try {
            ResultSet rs = MySQL.execute("SELECT c.id, c.name, c.company, c.email, c.phone "
                    + "FROM client c "
                    + "WHERE c.user_id='" + userData.id + "' AND c.id='" + clientId + "'");
            if (rs.next()) {
                nameField.setText(rs.getString("name"));
                companyField.setText(rs.getString("company"));
                emailField.setText(rs.getString("email"));
                phoneField.setText(rs.getString("phone"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void loadUserSessionData() {
        userData = SessionManager.getUserSession();
    }

