    private void addClientBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addClientBtnActionPerformed
        String client_name = nameField.getText();
        String company = companyField.getText();
        String email = emailField.getText();
        String phone = phoneField.getText();

        Timestamp nowTime = CurrentTimeGenerator.getInstance().getCurrentTime();

        if (!Validator.isInputFieldValid("Client name", client_name)) {
            return;
        } else if (!Validator.isInputFieldValid("Company", company)) {
            return;
        } else if (!Validator.isInputFieldValid("Email", email)) {
            return;
        } else if (!Validator.isInputFieldValid("Phone", phone)) {
            return;
        }

        try {
            if (!isEditing) {
                MySQL.execute("INSERT INTO `client` (name, company, email, phone, created_at, user_id) VALUES "
                        + "('" + client_name + "','" + company + "','" + email + "','" + phone + "','" + nowTime + "','" + userData.id + "')");
                SuccessDialog.show("Client added successfully!");
            } else {
                MySQL.execute("UPDATE `client` "
                        + " SET name='" + client_name + "', company='" + company + "', email='" + email + "', phone='" + phone + "', updated_at='" + nowTime + "' WHERE id='" + clientId + "'");
                SuccessDialog.show("Client details updated successfully!");
            }
            if (clientsPanel != null) {
                SwingUtilities.invokeLater(() -> {
                    clientsPanel.loadClients();
                });
            }
            this.dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Please try again to add the same client.", "Client adding failed!", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_addClientBtnActionPerformed


