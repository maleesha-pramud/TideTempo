    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        String task_title = titleField.getText();
        ComboItem project = (ComboItem) projectCombo.getSelectedItem();

        Timestamp nowTime = CurrentTimeGenerator.getInstance().getCurrentTime();

        if (!Validator.isInputFieldValid("Task Title", task_title)) {
            return;
        } else if (!Validator.isComboBoxValid("Project", project.getId())) {
            return;
        }

        try {
            if (!isEditing) {
                MySQL.execute("INSERT INTO `task` (title, project_id, status_id, created_at) VALUES "
                        + "('" + task_title + "','" + project.getId() + "','" + 1 + "','" + nowTime + "')");
                SuccessDialog.show("Task added successfully!");
            } else {
                MySQL.execute("UPDATE `task` "
                        + " SET title='" + task_title + "', project_id='" + project.getId() + "', updated_at='" + nowTime + "' WHERE id='" + taskId + "'");
                SuccessDialog.show("Task updated successfully!");
            }
            if (tasksPanel != null) {
                SwingUtilities.invokeLater(() -> {
                    tasksPanel.loadTasks();
                });
            }
            this.dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Please try again to add the same task.", "Task adding failed!", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_createButtonActionPerformed


