    private void init() {
        if (isEditing) {
            headerLabel.setText("Edit Task Details");
            createButton.setText("Update Task");
            loadTaskData();
        } else {
            headerLabel.setText("Add Task Details");
            createButton.setText("Create Task");
        }
    }

    private void loadTaskData() {
        try {
            ResultSet rs = MySQL.execute("SELECT t.title, p.title AS project "
                    + "FROM task t "
                    + "INNER JOIN project p ON t.project_id = p.id "
                    + "WHERE t.id='" + taskId + "'");
            if (rs.next()) {
                titleField.setText(rs.getString("title"));

                for (ComboItem c: projectList) {
                    if(c.getName().equals(rs.getString("project"))) {
                        projectCombo.setSelectedItem(c);
                        break;
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void loadUserSessionData() {
        userData = SessionManager.getUserSession();
    }

    private void loadProjects() {
        projectList = new ArrayList<>();
        projectCombo.addItem(new ComboItem(0, "Select Project"));
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `project`");
            while (rs.next()) {
                int id = rs.getInt("id");
                String name = rs.getString("title");
                ComboItem project = new ComboItem(id, name);
                projectList.add(project);
                projectCombo.addItem(project);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

