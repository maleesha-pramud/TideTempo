    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String firstName = firstNameField.getText();
        String lastName = lastNameField.getText();
        String email = emailField.getText();
        String newPassword = newPasswordField.getText();
        String confirmPassword = confirmPasswordField.getText();
        String mobile = mobileField.getText();
        ComboItem gender = (ComboItem) genderCombo.getSelectedItem();
        String hourlyRate = hourlyRateField.getText();

        if (!Validator.isInputFieldValid("First Name", firstName)) {
            return;
        } else if (!Validator.isInputFieldValid("Last Name", lastName)) {
            return;
        } else if (!Validator.isEmailValid(email)) {
            return;
        } else if (!Validator.isMobileValid(mobile)) {
            return;
        } else if (!Validator.isComboBoxValid("Gender", gender.getId())) {
            return;
        } else if (!Validator.isInputFieldValid("Hourly Rate", hourlyRate)) {
            return;
        } else if (!Validator.isConfirmPassword(newPassword, confirmPassword)) {
            return;
        }

        try {
            String query;
            if (!newPassword.isBlank() && !confirmPassword.isBlank()) {
                query = "UPDATE `user` "
                        + " SET f_name='" + firstName + "', l_name='" + lastName
                        + "', email='" + email + "', mobile='" + mobile + "', password='" + newPassword + "', gender_id='"
                        + gender.getId() + "', hourly_rate='" + Double.parseDouble(hourlyRate.replace(",", "")) + "' WHERE id='" + userData.id + "'";
            } else {
                query = "UPDATE `user` "
                        + " SET f_name='" + firstName + "', l_name='" + lastName
                        + "', email='" + email + "', mobile='" + mobile + "', gender_id='"
                        + gender.getId() + "', hourly_rate='" + Double.parseDouble(hourlyRate.replace(",", "")) + "' WHERE id='" + userData.id + "'";
            }
            MySQL.execute(query);
            SuccessDialog.show("User details updated successfully!");
            SwingUtilities.invokeLater(() -> {
                this.loadUserData();
                newPasswordField.setText("");
                confirmPasswordField.setText("");
            });
        } catch (SQLException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please try again to update user details.", "User details updating failed!", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


