    public void loadUserData() {

        try {
            ResultSet rs = MySQL.execute("SELECT u.id, u.f_name, u.l_name, u.email, u.mobile, g.name AS gender, u.hourly_rate "
                    + "FROM user u "
                    + "INNER JOIN gender g ON u.gender_id = g.id "
                    + "WHERE u.id='" + userData.id + "' "
                    + "ORDER BY u.id ASC");
            if (rs.next()) {
                firstNameField.setText(rs.getString("f_name"));
                lastNameField.setText(rs.getString("l_name"));
                emailField.setText(rs.getString("email"));
                mobileField.setText(rs.getString("mobile"));
                hourlyRateField.setText(rs.getString("hourly_rate"));

                for (ComboItem g : genderList) {
                    if (g.getName().equals(rs.getString("gender"))) {
                        genderCombo.setSelectedItem(g);
                        break;
                    }
                }

            } else {
                System.err.println("No user found with ID: " + userData.id);
//                homeScreen.setVisible(false);
//                authScreen.setVisible(true);
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void loadGenders() {
        genderList = new ArrayList<>();
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `gender`");
            while (rs.next()) {
                int id = rs.getInt("id");
                String name = rs.getString("name");
                ComboItem gender = new ComboItem(id, name);
                genderList.add(gender);
                genderCombo.addItem(gender);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

